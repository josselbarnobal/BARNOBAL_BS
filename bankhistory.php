<h2>Safe in the temple: 18th century BC</h2>

Wealth compressed into the convenient form of gold brings one disadvantage. <br />
Unless well hidden or protected, it is easily stolen.

In early civilizations a temple is considered the safest refuge; it is a solid building, <br />
constantly attended, with a sacred character which itself may deter thieves.<br /> 
In Egypt and Mesopotamia gold is deposited in temples for safe-keeping. <br />
But it lies idle there, while others in the trading community or in government have desperate need of it. <br />
In Babylon at the time of Hammurabi, in the 18th century BC, there are records of loans made by the priests of the temple.<br />
The concept of banking has arrived.<br /><br />

<h2>Greek and Roman financiers: from the 4th century BC</h2>

Banking activities in Greece are more varied and sophisticated than in any previous society. <br />
Private entrepreneurs, as well as temples and public bodies, now undertake financial transactions. <br />
They take deposits, make loans, change money from one currency to another and test coins for weight and purity.<br />

They even engage in book transactions. <br />
Moneylenders can be found who will accept payment in one Greek city and arrange for credit in another,<br />
 avoiding the need for the customer to transport or transfer large numbers of coins.<br /><br />
	  	


Rome, with its genius for administration, adopts and regularizes the banking practices of Greece.<br /> 
By the 2nd century AD a debt can officially be discharged by paying the appropriate sum into a bank,<br /> 
and public notaries are appointed to register such transactions.

The collapse of trade after the fall of the Roman empire makes bankers less necessary than before, <br />
and their demise is hastened by the hostility of the Christian church to the charging of interest.<br /> 
Usury comes to seem morally offensive. One anonymous medieval author declares vividly that 'a usurer <br />
is a bawd to his own money bags, taking a fee that they may engender together'.

